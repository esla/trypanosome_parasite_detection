def evaluate_test_only(model, data_loader, device="gpu"):
    n_threads = torch.get_num_threads()
    # FIXME remove this and make paste_masks_in_image run on the GPU
    torch.set_num_threads(1)
    cpu_device = torch.device("cpu")
    model.eval()
    metric_logger = utils.MetricLogger(delimiter="  ")
    header = "Test:"

    coco = get_coco_api_from_dataset(data_loader.dataset)

    iou_types = _get_iou_types(model)
    coco_evaluator = CocoEvaluator(coco, iou_types)

    json_dict = {}
    freq = int(0.25 * len(data_loader))
    for i, (images, targets) in enumerate(metric_logger.log_every(data_loader, freq, header)):
        images = list(img.to(device) for img in images)

        if torch.cuda.is_available():
            torch.cuda.synchronize()
        model_time = time.time()
        outputs = model(images, targets)

        # Fixme: Temporarily use only. This has to be set in the config file or in args
        iou_thres = 0.3
        boxes = outputs[0]["boxes"]
        scores = outputs[0]["scores"]
        labels = outputs[0]["labels"]
        filtered_boxes_indices = nms(boxes, scores, iou_threshold=iou_thres)
        outputs[0]["boxes"] = boxes[filtered_boxes_indices]
        outputs[0]["scores"] = scores[filtered_boxes_indices]
        outputs[0]["labels"] = labels[filtered_boxes_indices]

        outputs = [{k: v.to(cpu_device) for k, v in t.items()} for t in outputs]
        model_time = time.time() - model_time

        res = {target["image_id"].item(): output for target, output in zip(targets, outputs)}

        evaluator_time = time.time()
        coco_evaluator.update(res)
        evaluator_time = time.time() - evaluator_time
        metric_logger.update(model_time=model_time, evaluator_time=evaluator_time)

        if i == 0:
            json_dict["annotations"] = coco_evaluator.coco_eval["bbox"].cocoDt.dataset["annotations"]
            json_dict["images"] = coco_evaluator.coco_eval["bbox"].cocoDt.dataset["images"]
            json_dict["categories"] = coco_evaluator.coco_eval["bbox"].cocoDt.dataset["categories"]
        else:
            # update the annotations but skip images without predictions. COCO API handles such cases
            if len(coco_evaluator.coco_eval["bbox"].cocoDt.dataset.keys()) > 0:
                json_dict["annotations"] += coco_evaluator.coco_eval["bbox"].cocoDt.dataset["annotations"]
                # update annotation ids
                json_dict["annotations"][-1]["id"] = len(json_dict["annotations"]) + 1
            else:
                continue

    # update the annotation ids
    for i in range(0, len(json_dict["annotations"])):
        json_dict["annotations"][i]["id"] = i + 1

    # gather the stats from all processes
    metric_logger.synchronize_between_processes()
    print("Averaged stats:", metric_logger)
    coco_evaluator.synchronize_between_processes()

    # accumulate predictions from all images
    coco_evaluator.accumulate()
    coco_evaluator.summarize()

    torch.set_num_threads(n_threads)
    return coco_evaluator, json_dict